// Helper functions
export function getNicheBySlug(slug: string): Niche | undefined {
  return niches.find((niche) => niche.slug === slug)
}

export function getFirmsForNiche(niche: Niche): PropFirm[] {
  // Dynamic import to avoid circular dependency at module load time
  // eslint-disable-next-line @typescript-eslint/no-require-imports
  const firmsModule = require("./firms") as { firms: PropFirm[] }
  let filtered = firmsModule.firms.filter((f) => f.isActive).filter(niche.filterFn)

  if (niche.sortFn) {
    filtered = filtered.sort(niche.sortFn)
  } else {
    // Default: partners first, then by rating
    filtered = filtered.sort((a, b) => {
      if (a.isPartner && !b.isPartner) return -1
      if (!a.isPartner && b.isPartner) return 1
      return b.rating - a.rating
    })
  }

  return filtered
}

export function getNichesByCategory(): Record<NicheCategory, Niche[]> {
  const result: Partial<Record<NicheCategory, Niche[]>> = {}

  for (const niche of niches) {
    if (!result[niche.category]) {
      result[niche.category] = []
    }
    result[niche.category]!.push(niche)
  }

  return result as Record<NicheCategory, Niche[]>
}

export function getRelatedNiches(niche: Niche): Niche[] {
  if (!niche.relatedNiches) return []
  return niche.relatedNiches
    .map((slug) => getNicheBySlug(slug))
    .filter((n): n is Niche => n !== undefined)
    .slice(0, 6)
}

export function getNichesByCategorySlug(categorySlug: NicheCategory): Niche[] {
  return niches.filter((n) => n.category === categorySlug)
}

export function getAllCategories(): { slug: NicheCategory; name: string; count: number }[] {
  const categories = Object.entries(nicheCategories).map(([slug, data]) => ({
    slug: slug as NicheCategory,
    name: data.name,
    count: niches.filter((n) => n.category === slug).length,
  }))
  return categories.sort((a, b) => b.count - a.count)
}
