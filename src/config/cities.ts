import { hashString, seededRandom } from "@/lib/utils"

export interface Wijk {
  name: string
  slug: string
}

export interface City {
  id: number
  name: string
  slug: string
  province: string
  inhabitants: number
  priority: 1 | 2 | 3
  isLive: boolean
  wijken: Wijk[]
  image?: string
  imageAlt?: string
}

// Generate consistent fake statistics for cities
export function generateCityStats(city: City) {
  const baseTraders = Math.floor(city.inhabitants / 800)
  const variance = (hashString(city.slug) % 20) - 10 // -10% tot +10%
  const tradersActive = Math.floor(baseTraders + (baseTraders * variance) / 100)

  return {
    tradersActive,
    accountsOpened: Math.floor(tradersActive * 0.4),
    avgAccountSize: 25000 + seededRandom(city.slug + "-account", 0, 25000),
    popularFirm: city.priority === 1 ? "FTMO" : "Apex",
    successRate: 12 + seededRandom(city.slug + "-success", 0, 8), // 12-20%
    avgMonthlyProfit: 2500 + seededRandom(city.slug + "-profit", 0, 2500),
  }
}

// Generate wijk stats
export function generateWijkStats(wijk: Wijk, city: City) {
  const cityStats = generateCityStats(city)
  const wijkShare = seededRandom(wijk.slug, 5, 20) // 5-20% of city traders

  return {
    tradersActive: Math.floor((cityStats.tradersActive * wijkShare) / 100),
    popularFirm: seededRandom(wijk.slug + "-firm", 0, 10) > 5 ? "FTMO" : "Apex",
  }
}

