// Helper functions
export function getCityBySlug(slug: string): City | undefined {
  return cities.find((city) => city.slug === slug)
}

export function getCitiesByPriority(priority: 1 | 2 | 3): City[] {
  return cities.filter((city) => city.priority === priority && city.isLive)
}

export function getTopCities(count: number = 10): City[] {
  return cities
    .filter((city) => city.isLive)
    .sort((a, b) => a.priority - b.priority || b.inhabitants - a.inhabitants)
    .slice(0, count)
}

export function getAllCities(): City[] {
  return cities.filter((city) => city.isLive)
}

export function getWijkBySlug(citySlug: string, wijkSlug: string): Wijk | undefined {
  const city = getCityBySlug(citySlug)
  return city?.wijken.find((wijk) => wijk.slug === wijkSlug)
}
